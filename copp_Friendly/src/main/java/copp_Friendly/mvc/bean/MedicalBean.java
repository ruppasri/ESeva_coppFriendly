package copp_Friendly.mvc.bean;

public class MedicalBean {
	private String numem;
	private String numaffec;
	private String longitude;
	private String latitude;
	public String getNumem() {
		return numem;
	}
	public void setNumem(String numem) {
		this.numem = numem;
	}
	public String getNumaffec() {
		return numaffec;
	}
	public void setNumaffec(String numaffec) {
		this.numaffec = numaffec;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

}
